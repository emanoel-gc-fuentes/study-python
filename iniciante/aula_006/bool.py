# TIPOS DE DADOS: BOOLEAN ou Booleano
# Ao questionar algo em um programa, só existem duas respostas possíveis: # sim (True) ou não (False).

# Ex.:

print(10 == 10)  # Sim => True (Verdadeiro)

print(10 == 11)  # Não => False (Falso)

print(type(True))

print(type(False))

print(type(10 == 10))

print(type(10 == 11))